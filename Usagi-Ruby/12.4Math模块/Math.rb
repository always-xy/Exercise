math模块提供了三角函数，对数函数等常用的函数运算方法。

--------------------------------------
|	方法名	 	|		作用			 |
--------------------------------------
|	acos(x)		|	反余弦函数 		 |
--------------------------------------
|	acosh(x)	|	反双曲余弦函数 	 |
--------------------------------------
|	asin(x)		|	反正弦函数 		 |
--------------------------------------
|	asinh(x)	|	 反双曲正弦函数	 |
--------------------------------------
|	atan(x) 	|	反正切函数 		 |
--------------------------------------
|	atan2(x,y)	| 表示4个象限的反正切函数|
--------------------------------------
|	atanh(x)	|	反双曲正切函数		 |
--------------------------------------
|	cbrt(x)		|	立方根 			 |
--------------------------------------
|	cos(x)		| 	余弦函数 			 |
--------------------------------------
|	cosh(x)		|	 双曲余弦函数		 |
--------------------------------------
|	erf(x)		|	 误差函数			 |
--------------------------------------
|	erfc(x) 	|	余补误差函数		 |
--------------------------------------
|	exp(x)		|	 指数函数			 |
----------------------------------------
|	frexp(x)	|把一个浮点数分解为尾数和指数|
----------------------------------------
|	gamma(x)	|	 伽玛函数 		 |
--------------------------------------
|	hypot(x,y) 	|	计算三角形斜边长度 	 |
--------------------------------------
|	ldexp(x,y)	| 返回x乘以2的y次幂的值  |
--------------------------------------
|	lgamma(x) 	|	伽玛函数的自然对数 	 |
--------------------------------------
|	log(x) 		|底数为e的对数（自然对数）|
--------------------------------------
|	log10(x) 	|底数为10的对数（常用对数）|
--------------------------------------
|	log2(x) 	|	底数为2的对数 		  |
--------------------------------------
|	sin(x) 		|	正弦函数 			 |
--------------------------------------
|	sinh(x) 	|	双曲正弦函数 		 |
--------------------------------------
|	sqrt(x) 	|	平方根 			 |
--------------------------------------
|	tan(x) 		|	正切函数 			 |
--------------------------------------
|	tanh(x) 	|	双曲正切函数 		 |
--------------------------------------


