# 解释说明数值的字面量

 ---------------------------------------------------
 |        字面量       |    作用（括号内为10进制的值）   |
 |--------------------------------------------------
 |     123			  |    表示10进制整数             |
 |--------------------------------------------------
 |     0123           |    表示8进制整数              |
 |--------------------------------------------------
 |     0o123          |    表示8进制整数              |
 |--------------------------------------------------
 |     0d123          |    表示10进制整数             |
 |--------------------------------------------------
 |     0x123          |    表示16进制整数             |
 |--------------------------------------------------
 |     0b1111011      |    表示2进制整数              |
 |--------------------------------------------------
 |     123.45         |    浮点小数                  |
 |--------------------------------------------------
 |     1.23e4         |    浮点小数的指数表示法        |
 |--------------------------------------------------|
 |     1.23e-4        |    浮点小数的指数表示法        |
 ---------------------------------------------------
