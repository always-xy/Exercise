#最简单的字符串创建方法是把字符串集合用“” 或者''扩起来
str = "我是字符串"
str = '我也什字符串'

#在字符串当中可以使用#{}扩起来ruby式子， 称为内嵌式表达式。
moji = "字符串"
str = "我是#{moji}"
p str


